function y=nonzeros_gen(x)
  if isa(x,'casadi.SX') || isa(x,'casadi.MX') || isa(x,'casadi.DM')
    y = x{:};
  elseif isa(x,'sdpvar')
    b = getbase(x);
    f = find(sum(b~=0,2));
    y = sdpvar(length(f),1,[],getvariables(x),b(f,:));
  else
    y = nonzeros(x);
  end
end
